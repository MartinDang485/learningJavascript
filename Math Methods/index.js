let x = 3.14;

//Rounding Math.round
//Math.floor rounds down
//Math.ceil rounds up
x = Math.ceil(x);
console.log(x);

//Powers
//Math.pow takes exponent
//Math.sqrt takes root
//Math.abs is absolute value
x = Math.abs(x,2);
console.log(x);

//Max and Mins
//returns either max or min value
let y = 5;
let z = 9;
let maximum;
let minimum;

maximum = Math.max(y,z);
minimum = Math.min(y,z);
console.log(maximum); //9
console.log(minimum); //5

//Const for pi
x = Math.PI;
console.log(x);
